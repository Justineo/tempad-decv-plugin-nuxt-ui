type CamelToKebab<T extends string> = T extends `${infer First}${infer Rest}`
  ? `${Lowercase<First>}${CamelToKebabInner<Rest>}`
  : T

type CamelToKebabInner<T extends string> = T extends `${infer First}${infer Rest}`
  ? First extends Uppercase<First>
    ? `-${Lowercase<First>}${CamelToKebabInner<Rest>}`
    : `${First}${CamelToKebabInner<Rest>}`
  : T

